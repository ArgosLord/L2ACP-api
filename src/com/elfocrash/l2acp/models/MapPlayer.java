package com.elfocrash.l2acp.models;

public class MapPlayer {
    public String Name;
    public String Title;
    public int X;
    public int Y;
    public int Level;
    
    public MapPlayer(String name, String title, int level, int x, int y){
    	Name = name;
    	Title = title;
    	Level = level;
    	X = x;
    	Y = y;
    }
}
